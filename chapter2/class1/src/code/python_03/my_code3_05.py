#在while中应用else
i = 0
while i < 10:
    print('小明正在吃苹果')
    i += 1
else:#else分支
    print('小明吃完苹果了！')

#else在循环的判断条件为False的情况下执行else语句


#在for循环中使用else
food = ['鱼香肉丝','青椒肉丝','蚂蚁上树','西红柿炒蛋','乱棒打死猪八戒']

for i in food:
    print(i)
else:#else分支
    print('所有饭菜都点完了！')

