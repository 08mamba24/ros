#1.算术运算
# + 加法运算符
result = 88 + 77
print(result)

# - 减法运算
result = 88 - 77
print(result)

# * 乘法运算符
result = 9 * 11
print(result)

# / 除法运算符(浮点类型)
result = 100 / 5
print(result)

# ** 幂运算
result = 5 ** 4#result = 5 * 5 * 5 * 5
print(result)

# // 地板除运算符
result = 47 // 10
print(result)

# % 取余运算符
result = 47 % 10
print(result)

