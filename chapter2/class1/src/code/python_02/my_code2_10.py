#比较运算符
#1. >  大于运算符
result = 77 > 155
print(result)

#2. < 小于运算符
result = 14 < 19
print(result)

#3. >= 大于等于运算符
result = 15 >= 54
print(result)

#4. <= 小于等于运算
result = 45 <= 16
print(result)

#5. == 等于运算符 (六大类型不相同就不等于)
result = 55 == '55'
print(result)

#5. !=  不等于运算符
result = 55 != 55
print(result)

#扩展：连续的比较运算

result = 44 < 55 < 33 # -> 44 < 55 并且 55 < 33
print(result)