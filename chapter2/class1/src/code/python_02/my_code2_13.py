#1，按位与运算
var1 = 25#11001
var2 = 12#01100
result = var1 & var2
print(result)

#2.按位或运算
var1 = 25
var2 = 12
result = var1 | var2
print(result)

#3.按位非运算
var = 8 #1000
result = ~var
print(result)

#4.按位异或运算
var1 = 25
var2 = 12
result = var1 ^ var2
print(result)

#5.左移运算 相当于乘以2的运算
var = 48
result = var << 2
print(result)

#6.右移运算  相当于地板除2
var = 36
result = var >> 3
print(result)